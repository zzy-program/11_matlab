function z = raise_to_power(val,exp)
%RAISE_TO_POWER Calculate power of a value
%   z = raise_to_power(val,exp) raise val to a power with value of exp
%   and store it in z.

%   Copyright 2011 O. Marques
%   Practical Image and Video Processing Using MATLAB, Wiley-IEEE, 2011.
%   $Revision: 1.0 Date: 2011/06/21 13:12:00 

%perform calculation
z = val ^ exp;